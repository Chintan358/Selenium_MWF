package opartors;

public class O002_Arithmatic {
	public static void main(String[] args) {
		
		
		double a = 15;
		double b = 4;
		
		System.out.println(a+b);
		System.out.println(a+"tops");
		System.out.println(a+b+"tops");
		System.out.println("tops"+a+b);
		
		System.out.println(a*b);
		System.out.println(b/a);
		System.out.println(a-b);
		System.out.println(a%b);
		
		
	}
}
